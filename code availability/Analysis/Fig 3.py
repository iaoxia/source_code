def calcStaticParam2(x,y):
    sumMAE = 0
    sumRMSE = 0
    sumMBE = 0
    xBar = np.mean(x)
    yBar = np.mean(y)
    SSR = 0
    varX = 0
    varY = 0
    for i in range(len(x)):
        sumMAE = sumMAE + abs(y[i]-x[i])
        sumMBE = sumMBE + (y[i]-x[i])
        sumRMSE = sumRMSE + (y[i]-x[i])**2
        SSR = SSR + (y[i] - yBar) * (x[i] - xBar)
        varX = varX + (x[i] - xBar)**2
        varY = varY + (y[i] - yBar)**2
    MAENum = sumMAE/len(x)
    MAENum = round(MAENum,5)
    RMSENum = sqrt(sumRMSE/len(x))
    RMSENum = round(RMSENum,5)
    MBENum = sumMBE/len(x)
    MBENum = round(MBENum,5)
    SST = sqrt(varX * varY)
    r2 = round(SSR/SST,5)
    return r2


